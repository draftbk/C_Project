def get_enthalpy():
    s = os.popen("lmp_mpi <lammps.in")
    info = s.readlines()
    s.close()
    # print "-------输出焓--------"
    tag = 0
    for line in info:  # 按行遍历
        line = line.strip('\r\n')
        if (tag == 1):
            # print "energy: "+line
            return line
        if (line == "enthalpy"):
            tag = 1
    return 0

